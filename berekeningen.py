def math_1(plus):
  return 10 + plus

def math_2(minus):
  return 58 - minus

def math_3(multiply):
  return 6 * multiply

def math_4(divide):
  return 144 / (divide)

def math_5(plus_1):
  return 12 + (plus_1)

def math_6(minus_1):
  return 34 - (minus_1)

